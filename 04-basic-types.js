// Data (for example variables) can be of different types

let myNumber = 5; // Type number
let myOtherNumber = 12.47; // Type number

let myText = 'uwu'; // Type string
let myOtherText = "u.u"; // Type string

let myBoolean = true; // Type boolean (can be true or false)
let myOtherBoolean = false; // Type boolean


// Using typeof to see a type
console.log(typeof 125); // -> number
console.log(typeof 'AYAYA'); // -> string
console.log(typeof false); // -> boolean

